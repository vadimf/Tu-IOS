import UIKit

extension UIColor {
    
    class func tuttnauerRed() -> UIColor {
        return UIColor(red: 0.99, green: 0.00, blue: 0.02, alpha: 1.00)
    }
    
    class func tuttnauerOrange() -> UIColor {
        return UIColor(red: 0.97, green: 0.68, blue: 0.28, alpha: 1.00)
    }
    
    class func tuttnauerGreen() -> UIColor {
        return UIColor(red: 0.55, green: 0.79, blue: 0.25, alpha: 1.00)
    }
    
    class func tuttnauerGrey() -> UIColor {
        return UIColor(red: 0.92, green: 0.92, blue: 0.93, alpha: 1.00)
    }
    
}

